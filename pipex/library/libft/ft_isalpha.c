#include "libft.h"

int	ft_isalpha(int ch)
{
	int		flag;

	flag = 1;
	if (!((ch >= 65 && ch <= 90) || (ch >= 97 && ch <= 122)))
	{
		flag = 0;
	}
	return (flag);
}
