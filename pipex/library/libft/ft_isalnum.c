#include "libft.h"

int	ft_isalnum(int ch)
{
	int	flag;

	flag = 1;
	if (!(ch >= 48 && ch <= 57)
		&& !((ch >= 65 && ch <= 90) || (ch >= 97 && ch <= 122)))
		flag = 0;
	return (flag);
}
